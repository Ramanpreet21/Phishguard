"""
train.py
========
Trains 6 phishing-detection models and saves them to src/models/artifacts/.

Unified data pipeline — ALL models train on features derived from the
same source dataset (phishing_site_urls.csv via extracted_features.parquet).

Dataset layout expected in data/:
  data/phishing_site_urls.csv         → columns: URL, Label  (549k rows)
  data/extracted_features.parquet     → 22 URL-derived features + label
      (generated by: python extract_features.py)

Model split:
  Structured ML (trained on 22 URL-derived features):
    → rf.pkl   Random Forest
    → xgb.pkl  XGBoost
    → svm.pkl  Support Vector Machine

  Multimodal Deep Learning (char-level text + same 22 tabular features):
    → lstm.pt        Bidirectional LSTM + TabularBranch
    → cnn.pt         Character-level CNN + TabularBranch
    → transformer.pt Transformer encoder + TabularBranch

Also saves:
  scaler.pkl         StandardScaler for URL features (used by all models)
  feature_cols.pkl   List of feature column names (= URL_FEATURE_NAMES)
  fusion_weights.pkl Dict of model_name → float weight (F1-based)

Usage:
  python train.py [--features data/extracted_features.parquet]
                  [--csv data/phishing_site_urls.csv]
                  [--sample 50000]
                  [--epochs 10]
                  [--device cpu]
"""

